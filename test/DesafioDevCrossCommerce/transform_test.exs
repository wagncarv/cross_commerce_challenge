defmodule DesafioDevCrossCommerce.TransformTest do
    use DesafioDevCrossCommerce.DataCase, async: true
    alias DesafioDevCrossCommerce.Transform

    describe "transform/1" do
        test "When list is not sorted, sorts it" do
            list = [
                0.4181707133672159, 0.4446014940576127, 0.4135426519101487, 0.3364303523294636,
                0.2245623856417824, 0.6501239000539522, 0.6354200131296106, 0.5998745794215936,
                0.5000394301387743, 0.038535707538389585, 0.015621683926585785,
                0.11919252546814178, 0.5887652298728361, 0.3039725579091457,
                0.1774558105104917, 0.42900248202339797, 0.2601173525819016
            ]

            expected_result = Transform.transform(list)

            assert expected_result == [
                0.015621683926585785,
                0.038535707538389585,
                0.11919252546814178,
                0.1774558105104917,
                0.2245623856417824,
                0.2601173525819016,
                0.3039725579091457,
                0.3364303523294636,
                0.4135426519101487,
                0.4181707133672159,
                0.42900248202339797,
                0.4446014940576127,
                0.5000394301387743,
                0.5887652298728361,
                0.5998745794215936,
                0.6354200131296106,
                0.6501239000539522
              ]
        end
    end
end
